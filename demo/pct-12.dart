void main() {
  int number = 1;
  switch (number) {
    case -2:
      print('negative value even');
      break;
    case -1:
      print('negative is odd');
      break;
    case 0:
      print('even');
      break;
    case 1:
      print('odd');
      break;
    default:
      print('not identified');
  }
}
